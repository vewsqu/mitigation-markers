# =========================
# 4. ЛЕММАТИЗАЦИЯ
# =========================

def lemmatize(text):
    clean_text = re.sub(r'[^\w\s-]', '', text)
    
    words = clean_text.lower().split()
    lemmas = []

    for w in words:
        parsed = morph.parse(w)[0]
        lemmas.append(parsed.normal_form)

    return lemmas

# =========================
# 5. ПОИСК МАРКЕРОВ
# =========================
def find_markers(text):
    lemmas = lemmatize(text)
    found = []

    for mtype, markers in MARKERS.items():
        for marker in markers:

            marker_words = marker.split()
            m_len = len(marker_words)

            for i in range(len(lemmas) - m_len + 1):
                if lemmas[i:i+m_len] == marker_words:
                    found.append((marker, mtype))

    return found

# =========================
# 6. ОБРАБОТКА ТЕКСТОВ
# =========================
def process_texts(texts):

    results = []
    text_stats = []

    for i, text in enumerate(texts):

        markers = find_markers(text)
        word_count = len(text.split())

        # статистика по тексту
        text_stats.append({
            "text_id": i,
            "text": text,
            "word_count": word_count,
            "marker_count": len(markers)
        })

        # найденные маркеры
        for m, t in markers:
            results.append({
                "text_id": i,
                "marker": m,
                "type": t
            })

    df_markers = pd.DataFrame(results)
    df_texts = pd.DataFrame(text_stats)

    # =========================
    # 7. ПЛОТНОСТЬ
    # =========================
    df_texts["density"] = df_texts["marker_count"] / df_texts["word_count"] * 1000

    # =========================
    # 8. СОХРАНЕНИЕ
    # =========================
    df_markers.to_csv("markers8.csv", index=False)
    df_texts.to_csv("texts_stats8.csv", index=False)

